//For Addition
function getAdd(first, second) {
    return first + second;
}
console.log(getAdd(4, 4));
//For Subtraction
function getMinus(first, second) {
    return first - second;
}
console.log(getMinus(12, 4));
//For Multiplication
function getMulti(first, second) {
    return first * second;
}
console.log(getMulti(4, 2));
//For Division
function getDiv(first, second) {
    return first / second;
}
console.log(getDiv(32, 4));
